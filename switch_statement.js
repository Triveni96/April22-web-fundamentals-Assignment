let day2 = "Monday"
let day3 = "Tuesday"
let day4 = "wednessday"
let day5 = "thursday"
let day6 = "friday"
let day7 = "saturday"

let option = day2;
switch(option)
{
    case day1:
            console.log("sunday");
            break;
    case day2:
            console.log("monday");
            break;
    case day3:
            console.log("Tuesday");
            break;
    case day4:
            console.log("Wednessday");
            break;
    case day5:
            console.log("Thursday");
            break;
    case day6:
            console.log("Friday");
            break;
    case day7:
            console.log("saturday");
            break;
    default:
            console.log("Invalid day");

}
